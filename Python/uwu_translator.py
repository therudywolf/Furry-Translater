class UwuTranslator:
    """
    Класс для трансформации текста в UWU-стиль.
    
    Алгоритм трансформации:
    1. Словарные замены (приоритет) - замена слов по словарю
    2. Фонетические мутации (русский) - искажение звуков
    3. Фонетические мутации (английский) - искажение звуков
    4. Добавление волчьих звуков и кибер-символов
    """
    
    def __init__(self, 
                 noise_probability: float = 0.6,
                 cyber_vibe_probability: float = 0.3):
        """
        Инициализация трансформатора.
        
        Args:
            noise_probability: Вероятность добавления волчьих звуков (0.0-1.0)
            cyber_vibe_probability: Вероятность добавления кибер-символов (0.0-1.0)
        """
        self.noise_probability = noise_probability
        self.cyber_vibe_probability = cyber_vibe_probability
        self.dictionary = UWU_DICT.copy()
        self.wolf_noises = WOLF_NOISES.copy()
        self.cyber_vibes = CYBER_VIBES.copy()
    
    def translate(self, text: str) -> str:
        """
        Основная функция трансформации текста.
        
        Args:
            text: Исходный текст для трансформации
            
        Returns:
            Текст в UWU-стиле
        """
        if not text or not text.strip():
            return text
        
        # Шаг 1: Словарные замены (приоритет - выполняется первым)
        result = self._apply_dictionary_replacements(text)
        
        # Шаг 2: Фонетические мутации для русского языка
        result = self._apply_russian_phonetics(result)
        
        # Шаг 3: Фонетические мутации для английского языка
        result = self._apply_english_phonetics(result)
        
        # Шаг 4: Добавление волчьих звуков и кибер-символов
        result = self._add_wolf_noises_and_cyber_vibes(result)
        
        return result
    
    def _apply_dictionary_replacements(self, text: str) -> str:
        """
        Применяет словарные замены слов.
        
        Использует регулярные выражения для замены целых слов,
        чтобы не заменять части слов.
        
        Args:
            text: Исходный текст
            
        Returns:
            Текст с замененными словами
        """
        result = text
        for word, replacement in self.dictionary.items():
            # Используем \b для границ слов, чтобы не заменять части слов
            # re.IGNORECASE для регистронезависимой замены
            pattern = fr'\b{re.escape(word)}\b'
            result = re.sub(pattern, replacement, result, flags=re.IGNORECASE)
        return result
    
    def _apply_russian_phonetics(self, text: str) -> str:
        """
        Применяет фонетические мутации для русского языка.
        
        Правила:
        - 'р' -> 'ль' (мягкое произношение)
        - 'л' -> 'лью' (милое искажение)
        - Добавление мягкого знака после согласных перед гласными
        
        Args:
            text: Текст после словарных замен
            
        Returns:
            Текст с примененными фонетическими мутациями
        """
        result = text
        
        # Замена 'р' на 'ль' (мягкое произношение)
        result = result.replace('р', 'ль').replace('Р', 'Ль')
        
        # Замена 'л' на 'лью' (милое искажение)
        # Исключаем случаи, когда 'л' уже часть 'ль' или 'лю'
        result = result.replace('л', 'лью').replace('Л', 'Лью')
        
        # Добавление мягкого знака после согласных перед гласными
        # Паттерн: согласная (в, н, с, з) + гласная -> согласная + ь + гласная
        result = re.sub(
            r'([внсз])([аеёиоуыэюя])',
            r'\1ь\2',
            result,
            flags=re.IGNORECASE
        )
        
        return result
    
    def _apply_english_phonetics(self, text: str) -> str:
        """
        Применяет фонетические мутации для английского языка.
        
        Правила:
        - Одиночные 'r' и 'l' -> 'w' (классический uwu)
        - 'n' + гласная -> 'ny' + гласная (милое произношение)
        
        Args:
            text: Текст после русских фонетических мутаций
            
        Returns:
            Текст с примененными английскими фонетическими мутациями
        """
        result = text
        
        # Замена одиночных 'r' и 'l' на 'w' (только если это отдельные символы)
        # Паттерн: не буква + r/l + не буква -> не буква + w + не буква
        result = re.sub(
            r'(?<![A-Za-zА-Яа-я])[rl](?![A-Za-zА-Яа-я])',
            'w',
            result
        )
        result = re.sub(
            r'(?<![A-Za-zА-Яа-я])[RL](?![A-Za-zА-Яа-я])',
            'W',
            result
        )
        
        # Замена 'n' + гласная на 'ny' + гласная
        result = re.sub(r'n([aeiou])', r'ny\1', result, flags=re.IGNORECASE)
        
        return result
    
    def _add_wolf_noises_and_cyber_vibes(self, text: str) -> str:
        """
        Добавляет волчьи звуки и кибер-символы после знаков препинания.
        
        Алгоритм:
        1. Разбивает текст на сегменты по знакам препинания
        2. После каждого знака препинания с определенной вероятностью
           добавляет волчьи звуки и/или кибер-символы
        
        Args:
            text: Текст после всех трансформаций
            
        Returns:
            Текст с добавленными звуками и символами
        """
        # Разбиваем текст на сегменты, сохраняя знаки препинания
        # Паттерн: разделяем по знакам препинания, но сохраняем их
        segments = re.split(r'([.!?…])', text)
        result_parts = []
        
        for segment in segments:
            if segment.strip():
                result_parts.append(segment)
                
                # Если сегмент - знак препинания, добавляем звуки
                if segment in '.!?…':
                    # Добавляем волчьи звуки с заданной вероятностью
                    if random.random() > (1 - self.noise_probability):
                        noise = random.choice(self.wolf_noises)
                        result_parts.append(noise)
                    
                    # Добавляем кибер-символы с заданной вероятностью
                    if random.random() > (1 - self.cyber_vibe_probability):
                        vibe = random.choice(self.cyber_vibes)
                        result_parts.append(vibe)
        
        return ''.join(result_parts)


