# Создаем глобальный экземпляр трансформатора для обратной совместимости
_default_translator = UwuTranslator()


def uwu_translate(text: str) -> str:
    """
    Функция-обертка для обратной совместимости со старым кодом.
    
    Использует глобальный экземпляр трансформатора с настройками по умолчанию.
    
    Args:
        text: Исходный текст для трансформации
        
    Returns:
        Текст в UWU-стиле
        
    Example:
        >>> uwu_translate("Привет, волк!")
        "Пльивветик~, уольчек! *хвост вильнул*"
    """
    return _default_translator.translate(text)

